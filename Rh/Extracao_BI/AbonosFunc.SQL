SELECT
	PKCHAPA = CONVERT(VARCHAR, PEDABONO.CODCOLIGADA) + '_' + UPPER(CONVERT(VARCHAR(25), PFUNC.CHAPA))
	,FKCODCOLIGADA = CONVERT(VARCHAR,GCOLIGADA.CODCOLIGADA)
	,[Data do Pedido] = CONVERT(VARCHAR(10), PEDABONO.RECCREATEDON, 112)
	,[Data Inicio] = CONVERT(VARCHAR(10), PEDABONO.DATAHORAINICIO, 112)
	,[Data Final] = CONVERT(VARCHAR(10), PEDABONO.DATAHORAFIM, 112)
	,[Data Aprovacao] = CONVERT(VARCHAR(10), PEDABONO.DATAAPROVACAO, 112)
	,[Situacao] = UPPER(CONVERT(VARCHAR(25), PEDABONO.SITUACAO))
	,[ID do Abono] = UPPER(CONVERT(VARCHAR(25), PEDABONO.ID))
	,[Desccricao do Abono] = UPPER(CONVERT(VARCHAR(25), AABONO.DESCRICAO))
	,[Justificatica] = UPPER(PEDABONO.JUSTIFICATIVA)

FROM AABONOFUNCIONARIO PEDABONO (NOLOCK)
    INNER JOIN GCOLIGADA 
        ON GCOLIGADA.CODCOLIGADA = PEDABONO.CODCOLIGADA
    LEFT JOIN PFUNC
        ON PFUNC.CHAPA = PEDABONO.CHAPA
	INNER JOIN AABONO
		ON PEDABONO.CODABONO = AABONO.CODIGO
		AND AABONO.CODCOLIGADA = PFUNC.CODCOLIGADA


